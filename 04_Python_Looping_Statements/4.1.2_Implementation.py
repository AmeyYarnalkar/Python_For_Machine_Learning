# ---------------------------------------------------------
#  Basic for loop using range()
# ---------------------------------------------------------

for i in range(5):
    print("Value:", i)


# ---------------------------------------------------------
#  for loop with start, stop, step
# ---------------------------------------------------------

for i in range(1, 10, 2):
    print(i)    # 1 3 5 7 9


# ---------------------------------------------------------
#  Looping through a string
# ---------------------------------------------------------

text = "python"

for ch in text:
    print(ch)


# ---------------------------------------------------------
#  while loop example
# ---------------------------------------------------------

i = 0

while i < 3:
    print("i =", i)
    i += 1


# ---------------------------------------------------------
#  Even or odd using loop
# ---------------------------------------------------------

for num in range(1, 6):
    if num % 2 == 0:
        print(num, "is Even")
    else:
        print(num, "is Odd")


# ---------------------------------------------------------
# break example
# ---------------------------------------------------------

for i in range(5):
    if i == 3:
        break
    print(i)


# ---------------------------------------------------------
#  continue example
# ---------------------------------------------------------

for i in range(5):
    if i == 2:
        continue
    print(i)


# ---------------------------------------------------------
#  pass example
# ---------------------------------------------------------

for i in range(3):
    pass   # placeholder


# ---------------------------------------------------------
#  loop else example
# ---------------------------------------------------------

for i in range(3):
    print(i)
else:
    print("Loop finished normally")


# ---------------------------------------------------------
#  nested loops example
# ---------------------------------------------------------

for i in range(2):
    for j in range(3):
        print("i =", i, "j =", j)


# ---------------------------------------------------------
#  while True with break
# ---------------------------------------------------------

count = 0

while True:
    print("Running", count)
    count += 1
    if count == 3:
        break


# ---------------------------------------------------------
#  Iteration vs Indexing
# ---------------------------------------------------------

s = "loop"

# direct iteration (recommended)
for ch in s:
    print(ch)

# using index
for i in range(len(s)):
    print(s[i])
